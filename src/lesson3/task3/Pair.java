package lesson3.task3;

public class Pair <T, X>{
    private T first;
    private X second;
    public Pair(T first, X second){
        this.first = first;
        this.second = second;
    }



    public X getSecond() {
        return second;
    }



    public T getFirst() {
        return first;
    }

    public String toString(){
        return toString() ;
    }



}
